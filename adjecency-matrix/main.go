type matrix [][]int

func (m *matrix) String() {
	data := *m
	for i := range data {
		fmt.Println(data[i])
	}
}

func NewMatrix(height, width int) matrix {
	data := matrix{}
	for i := 0; i < height; i++ {
		row := []int{}
		for j := 0; j < width; j++ {
			row = append(row, 0)
		}
		data = append(data, row)
	}
	return data
}

